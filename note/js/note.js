function hidetext(){
       	var absdd=document.getElementById("con");
       	var absddd=document.getElementById("btns");
       	if(absdd.style.display=="none"){
       		absdd.style.display="block";
       		absddd.value="关闭";
       	}else{
       		absdd.style.display="none";
       		absddd.value="展开";
       	}
       	}
